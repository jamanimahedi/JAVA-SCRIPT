//  push() → Add element at the end
let subjects = ["Maths", "Science", "English"];

subjects.push("Computer");
console.log("After push:", subjects);


//  unshift() → Add element at the beginning
let colors = ["Blue", "Green", "Yellow"];

colors.unshift("Red");
console.log("After unshift:", colors);


//  pop() → Remove element from the end
let cities = ["Delhi", "Mumbai", "Chennai"];

cities.pop();
console.log("After pop:", cities);


//  shift() → Remove element from the beginning
let snacks = ["Burger", "Pizza", "Sandwich"];

snacks.shift();
console.log("After shift:", snacks);