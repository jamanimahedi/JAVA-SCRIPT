//  First / Last & Modification Example

//  First element changing

let scores = [55, 65, 75, 85, 95];

scores[0] = 60;   // change first value

console.log("After first score change:", scores);


//  Last element accessing

const cities = ["Delhi", "Mumbai", "Chennai", "Kolkata", "Jaipur"];

let lastCity = cities[cities.length - 1];

console.log("Last city is:", lastCity);


// 3 Modification (changing element in array)

let courses = ["HTML", "CSS", "JavaScript", "Bootstrap"];

courses[2] = "React";   // modifying 3rd element

console.log("After modification:", courses);