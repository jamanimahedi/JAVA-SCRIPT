// primitive data type//


// 1. Number Data Type

let num1 = 10;
let num2 = 25.75;

console.log(num1);
console.log(num2);


// 2. String Data Type

let str1 = "Hello";
let str2 = "JavaScript";

console.log(str1);
console.log(str2);

// 3. Boolean Data Type

let bool1 = true;
let bool2 = false;

console.log(bool1);
console.log(bool2);


// 4. Undefined Data Type

let u1;
let u2;

console.log(u1);
console.log(u2);


// 5. Null Data Type

let n1 = null;
let n2 = null;

console.log(n1);
console.log(n2);


// 6. BigInt Data Type

let big1 = 12345678901234567890n;
let big2 = 9007199254740991n;

console.log(big1);
console.log(big2);


// 7. Symbol Data Type

let sym1 = Symbol("id");
let sym2 = Symbol("user");

console.log(sym1);
console.log(sym2);


//non-primitive data type//

// 8. Object Data Type

let obj1 = { name: "Jamani", age: 21 };
let obj2 = { city: "Bhavnagar", state: "Gujarat" };

console.log(obj1);
console.log(obj2);


